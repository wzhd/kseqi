#[cfg(test)]
mod tests {
    use std::ffi::CString;

    use crate::xdl::Xdll;

    use super::*;

    #[test]
    fn kes() {
        let xl=Xdll::new().unwrap();
        for &(s, k) in DEFINED_SYM_KEYSYM.iter(){
            let cstr = &CString::new(s).unwrap();
            let x = xl.xdp.string_to_keysym(cstr);
            assert!(x.is_some(), "{s} has no ks, not {k}");
            let c = x.unwrap().get();
            assert_eq!(k, c as u32, "sym {s} is {c} instead of {k}");
        }
    }
}
